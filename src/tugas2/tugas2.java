    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String nama=txtnama.getText();
        String nim=txtnim.getText();
        String kelas=txtkelas.getText();
        String hp=txthp.getText();
        String ttl =txtttl.getText();
        String minat = (String) cmbminat.getSelectedItem();
        String jengkel=" ";
        if (rdpria.isSelected()){
            jengkel = rdpria.getText();
        }
        else if (rdwanita.isSelected()){
            jengkel = rdwanita.getText();
        }else{
            jengkel = rdlainnya.getText();
        }
        
        formtampil a = new formtampil(nama,nim,kelas,hp,minat,jengkel,ttl);
        a.setVisible(true);
        dispose();
       
    }//GEN-LAST:event_btnsimpanActionPerformed

